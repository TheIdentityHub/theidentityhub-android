package com.theidentityhub.model;

/**
 * The Class Role.
 * 
 * @author Marija Nikolovska
 * @since 1.0
 */
public class Role {

     /** The name. */
     private String name;

     /**
      * Instantiates a new role.
      * 
      * @param name
      *             the name
      */
     public Role(String name) {
          super();
          name = name;
     }

     /**
      * Gets the name.
      * 
      * @return the name
      */
     public String getName() {
          return name;
     }

}
